    public int GetByteOfBitLength(int bb)
    {
	int byteofbitlength = 0;
	if (bb > 0 && bb < 2)
	    byteofbitlength = 1;
	else if (bb >= 2 && bb < 4)
	    byteofbitlength = 2;
	else if (bb >= 4 && bb < 8)
	    byteofbitlength = 3;
	else if (bb >= 8 && bb <= 15)
	    byteofbitlength = 4;
	else if (bb >= 16 && bb <= 31)
	    byteofbitlength = 5;
	else if (bb >= 33 && bb <= 63)
	    byteofbitlength = 6;
	else if (bb >= 64 && bb <= 127)
	    byteofbitlength = 7;
	else if (bb >= 128 && bb <= 255)
	    byteofbitlength = 8;
	return byteofbitlength;
    }

